/**
 * Generate SHA-256 checksums for argon2 native module binaries.
 *
 * This script computes checksums of all prebuilt .node files and outputs
 * a JSON file that can be embedded in the bundled app for integrity verification.
 *
 * Usage:
 *   node scripts/generate-argon2-checksums.js
 *
 * The output file is written to src/main/crypto/argon2-checksums.json
 * and should be committed to version control.
 */

const { createHash } = require('crypto');
const { readdirSync, readFileSync, writeFileSync, statSync } = require('fs');
const { join, relative } = require('path');

const PACKAGE_ROOT = join(__dirname, '..');
const PREBUILDS_DIR = join(PACKAGE_ROOT, 'node_modules', 'argon2', 'prebuilds');
const OUTPUT_FILE = join(PACKAGE_ROOT, 'src', 'main', 'crypto', 'argon2-checksums.json');

function computeSha256(filePath) {
  const content = readFileSync(filePath);
  return createHash('sha256').update(content).digest('hex');
}

function scanPrebuilds() {
  const checksums = {};

  if (!statSync(PREBUILDS_DIR, { throwOnError: false })) {
    console.warn('[generate-argon2-checksums] Prebuilds directory not found');
    return checksums;
  }

  const platforms = readdirSync(PREBUILDS_DIR);

  for (const platform of platforms) {
    const platformDir = join(PREBUILDS_DIR, platform);
    const stat = statSync(platformDir, { throwOnError: false });

    if (!stat || !stat.isDirectory()) continue;

    const files = readdirSync(platformDir);

    for (const file of files) {
      if (!file.endsWith('.node')) continue;

      const filePath = join(platformDir, file);
      const relativePath = `prebuilds/${platform}/${file}`;
      const hash = computeSha256(filePath);

      checksums[relativePath] = hash;
      console.log(`  ${relativePath}: ${hash}`);
    }
  }

  return checksums;
}

function main() {
  console.log('[generate-argon2-checksums] Computing checksums for argon2 native modules...\n');

  const checksums = scanPrebuilds();
  const count = Object.keys(checksums).length;

  if (count === 0) {
    console.warn('[generate-argon2-checksums] No .node files found. Skipping checksum generation.');
    return;
  }

  // Create the output object with metadata
  const output = {
    _comment: 'SHA-256 checksums for argon2 native module binaries. Auto-generated by scripts/generate-argon2-checksums.js',
    _generatedAt: new Date().toISOString(),
    _packageVersion: require('../node_modules/argon2/package.json').version,
    checksums,
  };

  writeFileSync(OUTPUT_FILE, JSON.stringify(output, null, 2) + '\n');
  console.log(`\n[generate-argon2-checksums] Wrote ${count} checksums to ${relative(PACKAGE_ROOT, OUTPUT_FILE)}`);
}

main();
